// Constants for time intervals
export const secondsInMinute = 60;       // 60 секунд в минуте
export const secondsInHour = 3600;       // 3600 секунд в часе (60 секунд * 60 минут)
export const secondsInDay = 86400;       // 86400 секунд в дне (60 секунд * 60 минут * 24 часа)
export const secondsInWeek = 604800;     // 604800 секунд в неделе (60 секунд * 60 минут * 24 часа * 7 дней)
export const secondsInMonth = 2592000;   // 2592000 секунд в месяце (приближенно 30 дней * 24 часа * 60 минут * 60 секунд)
export const secondsInYear = 31536000;   // 31536000 секунд в году (приближенно 365 дней * 24 часа * 60 минут * 60 секунд)